package wang.ulane.json;

@StdNameAnnotation
@FieldBaseAnnotation
public class JsonBean {
	private String SDFD;
	
	private String SEF;
	
	private String sdfd;

	public String getSDFD() {
		return SDFD;
	}

	public void setSDFD(String sDFD) {
		SDFD = sDFD;
	}

	public String getSEF() {
		return SEF;
	}

	public void setSEF(String sEF) {
		SEF = sEF;
	}

	public String getSdfd() {
		return sdfd;
	}

	public void setSdfd(String sdfd) {
		this.sdfd = sdfd;
	}
	
}
